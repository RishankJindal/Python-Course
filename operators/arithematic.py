# Arithematic Operators

# '+' --> Addition
# '-' --> Subtraction
# '*' --> Multiplication (asterisk)
# '/' --> Division
# '//' --> Floor Division
# '%' --> Modulus (Remainder)
# '**' --> Exponentiation (Power)

num1 = 10
num2 = 5

print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 // num2)
print(num1 % num2)
print(num1 ** num2)